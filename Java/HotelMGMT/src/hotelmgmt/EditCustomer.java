    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        // TODO add your handling code here:
        try{
            if(UserControls.integerCheck(customerId.getText())){
                if(ht.checkCustomerId(Integer.valueOf(customerId.getText()))){
                    showDialogue(Integer.valueOf(customerId.getText()));              
                    this.dispose();
                }
                else{
                    UserView.speakInfo("Bad customer Id.");
                    this.dispose();
                }            
            }
            else{UserView.speakInvalid();}                             
            
        }
        catch(Exception e){
            UserView.speakError("Exception. Invalid input.");
            customerId.setText("");            
            return;            
        }
    }//GEN-LAST:event_okButtonActionPerformed

