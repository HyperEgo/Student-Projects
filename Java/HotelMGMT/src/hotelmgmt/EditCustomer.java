    /**
     * Show secondary dialogue to get further user information.
     * @param id customer id number, must be unique
     */
    public void showDialogue(int id){
//        ht = HotelSystem.getInstance();
        // recycled code from addEmployeeorCustomer - modified        
        JFrame frame = new JFrame();
        frame.setTitle("Edit Customer");
        frame.setLayout(new BorderLayout());
        frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        frame.setLocationRelativeTo(this);
        TextField name = new TextField("", 20);
        JLabel nameLabel = new JLabel("       Name: ");
        TextField pay = new TextField("", 20);
        JLabel payLabel = new JLabel("       Pay Type: ");
        
        JButton cancelButton = new JButton("Cancel");
        cancelButton.setVisible(true);
        cancelButton.addActionListener(new ActionListener(){
            public void actionPerformed(ActionEvent event){
                frame.dispose();
            }  // end actionPerformed
        });  // end cancelButton addActionListener
        
        JButton enterButton = new JButton("Enter");
        enterButton.setVisible(true);
        enterButton.addActionListener(new ActionListener(){
                public void actionPerformed(ActionEvent event){
                    if(UserControls.stringCheck(name.getText())&&(UserControls.stringCheck(pay.getText()))){  // check valid input
                        if(ht.editCustomer(id, name.getText(), pay.getText())){
                            UserView.speakInfo("Customer edit successful.");
                            UserView.listClear();
                            frame.dispose();                            
                        }                                                
                    }
                    else{
                        UserView.speakInvalid();
                        frame.dispose();
                    }                               
                }  // end actionPerformed
            });  // end enterButton addActionListener
        
        JPanel topPanel = new JPanel();
        topPanel.setLayout(new GridLayout(3, 4, 0, 10));
        topPanel.add(nameLabel);
        topPanel.add(name);
        topPanel.add(payLabel);
        topPanel.add(pay);
        frame.add(topPanel, BorderLayout.NORTH);   
        
        JPanel buttonPanel = new JPanel();
        buttonPanel.add(enterButton);
        buttonPanel.add(cancelButton);
        frame.add(buttonPanel, BorderLayout.SOUTH);

        frame.setPreferredSize(new Dimension(300, 200));
        frame.pack();
        frame.setVisible(true);
        
    }  // end showDialogue function    

